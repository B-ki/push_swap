#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	size_t			t_src;

	t_src = ft_strlen((char *)src);
	i = 0;
	if (size == 0)
		return (t_src);
	while (i < size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (t_src);
}
