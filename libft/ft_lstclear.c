#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	void	*tmp;

	if (*lst)
	{
		while (*lst)
		{
			tmp = (*lst)->next;
			free(*lst);
			*lst = tmp;
		}
	}
}
